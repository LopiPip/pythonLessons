# int - целые числа
num_1 = 10
print(num_1) # 10

# float - числа с плавающей точкой
num_2 = 2.5
print(num_2) # 2.5

# Преобразование данных
# int() - преобразует в целое число
num_3 = float(num_1)
print(num_3) # 10.0

# float - преобразует в число с плавающей точкой
num_4 = int(num_2)
print(num_4) # 2